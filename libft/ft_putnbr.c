#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int v;

	if (n < 0)
	{
		ft_putchar('-');
		v = (n * -1);
	}
	else
	{
		v = n;
	}
	if (v > 9)
		ft_putnbr(v / 10);
	ft_putchar(v % 10 + '0');
}
