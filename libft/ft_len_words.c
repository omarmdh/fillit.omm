#include "libft.h"

int	ft_len_words(char *str, char c)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (str[i] == c && str[i] != '\0')
	{
		while (str[i] == c)
			i++;
		if (str[i + 1] == '\0')
			return (0);
	}
	j = i + 1;
	while (str[j] && str[j] != c)
	{
		j++;
	}
	j = j - i;
	return (j);
}
