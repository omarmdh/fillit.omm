#include "libft.h"

char	*ft_strncpy(char *dest, const char *s, size_t n)
{
	size_t	i;
	char	*src;

	src = (char*)s;
	i = 0;
	while (i < n && src[i])
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}
