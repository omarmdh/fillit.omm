#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	int i;
	int o;

	i = 0;
	o = 0;
	while (dest[o] != '\0')
		o++;
	while (src[i] != '\0')
	{
		dest[o] = src[i];
		i++;
		o++;
	}
	dest[o] = '\0';
	return (dest);
}
